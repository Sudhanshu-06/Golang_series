package main
import "fmt"

func main(){
	// simple values
	// int
	fmt.Println(1)
	fmt.Println(1+1)

	// string
	fmt.Println("Hello, World!")

    // bool
	fmt.Println(true)
	fmt.Println(false)

	// floats
	fmt.Println(3.14)
    fmt.Println(3.14 + 3.14)
	fmt.Println(7.0/3.0)

    // complex numbers
    fmt.Println(2 + 3i)
    fmt.Println(2 + 3i + 2 + 3i)

    
    

}